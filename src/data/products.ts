export const searchProducts = (query: string): Product[] => {
  if (query.length < 2) return [];
  const lowerQuery = query.toLowerCase();
  
  const additionalTerms: string[] = [];
  Object.entries(searchKeywords).forEach(([key, values]) => {
    if (lowerQuery.includes(key)) {
      additionalTerms.push(...values);
    }
  });
  
  return allProducts.filter((product) => {
    const productName = product.name.toLowerCase();
    const productNameEn = product.nameEn.toLowerCase();
    const productCategory = product.category.toLowerCase();
    
    if (productName.includes(lowerQuery) || productNameEn.includes(lowerQuery) || productCategory.includes(lowerQuery)) {
      return true;
    }
    
    for (const term of additionalTerms) {
      if (productName.includes(term.toLowerCase()) || productCategory.includes(term.toLowerCase())) {
        return true;
      }
    }
    
    return false;
  });
};

